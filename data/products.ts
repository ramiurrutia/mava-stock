function formatProductSize(
  measureCode: ProductMeasureCode,
  finish: ProductFinish,
) {
  const baseSize = `${measureCode} ${measureSizeByCode[measureCode]}`;

  if (finish === "liso") {
    return baseSize;
  }

  return `${baseSize} / ${finishLabelById[finish]}`;
}

